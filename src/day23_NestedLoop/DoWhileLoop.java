package day23_NestedLoop;

public class DoWhileLoop {

    public static void main(String[] args) {

        boolean condition = false;

        while(condition){ //think first, if it's true or false
            System.out.println("Hello Batch 21");
        }

        System.out.println("===================================");

        do {
            System.out.println("Hello Batch 21");
        }while(condition); //do first,think after.
        //if condition is false it only prints once,but if true it prints forever.







    }

}
