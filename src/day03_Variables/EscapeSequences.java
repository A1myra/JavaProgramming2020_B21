package day03_Variables;

public class EscapeSequences {

    public static void main(String[] args) {
//\n:

        System.out.println("Hello Everyone");
        System.out.println("My name is Almira, I am from virginia");
//other solution
        System.out.println("=========================================");
        System.out.println("Hello Everyone\nMy name is Almira, I am from Virginia");


        System.out.println("=========================================");


 // \t:Paragraph Space
        System.out.println("\tToday I learned Java");
        System.out.println("It eas so much fun");


        System.out.println("=========================================");

 // \\:\
        System.out.println("/"); // Prints single front slash
        System.out.println("\\"); // Prints single back slash

        System.out.println("=========================================");

 // \' or ' single quote

        System.out.println("My name is 'Almira'");
        //or
        System.out.println("My name is \'Almira\'");


        System.out.println("=========================================");

 // \" double quote

        System.out.println("my favorite TV show is \"NBA\"");
          //this is the only way that I can print out double quote





    }
}

